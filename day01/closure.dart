/**
 * Dart闭包
 * 
 *    1.全局变量的特点：全局变量常驻内存、全局变量污染全局
 *    2.局部变量的特点：不常驻内存，会被垃圾回收机制回收、不会污染全局
 * 
 * 
 *    闭包：
 *        函数嵌套函数，内部函数会调用外部函数的变量或者参数
 *        闭包的写法：函数嵌套函数，并return里面的函数，这样就形成了闭包。
 */
void main() {
  printInfo() {
    var a = 123; //不会污染全局,可以常驻内存
    return () {
      a++;
      print(a);
    };
  }

  var b = printInfo();
  b();
}
